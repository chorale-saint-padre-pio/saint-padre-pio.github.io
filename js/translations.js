// Language detection and management
const Lang = {
  current: 'en',

  detect() {
    const saved = localStorage.getItem('cspp_lang');
    if (saved) return saved;
    const browser = navigator.language || navigator.userLanguage || 'en';
    return browser.startsWith('fr') ? 'fr' : 'en';
  },

  set(lang) {
    this.current = lang;
    localStorage.setItem('cspp_lang', lang);
    this.apply();
  },

  get(key) {
    return TRANSLATIONS[this.current][key] || TRANSLATIONS['en'][key] || key;
  },

  apply() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.get(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else {
        el.innerHTML = text;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.get(el.getAttribute('data-i18n-placeholder'));
    });
    // Update lang toggle
    const toggle = document.getElementById('lang-toggle');
    if (toggle) {
      toggle.textContent = this.current === 'en' ? 'FR' : 'EN';
      toggle.setAttribute('aria-label', this.current === 'en' ? 'Passer en français' : 'Switch to English');
    }
    document.documentElement.lang = this.current;
  },

  init() {
    this.current = this.detect();
    this.apply();
  }
};
